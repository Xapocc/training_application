import 'package:equatable/equatable.dart';

abstract class RouterState extends Equatable {
  const RouterState();

  @override
  List<Object?> get props => [];
}

class SplashScreenState extends RouterState {
  @override
  List<Object?> get props => [];
}

class MainScreenState extends RouterState {
  @override
  List<Object?> get props => [];
}